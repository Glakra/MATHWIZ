# Filter topics based on selected grade
def filter_topics_by_grade(topics_dict, selected_grade_display):
    """Filter topics to show only those relevant to the selected grade level"""
    filtered_topics = {}
    
    for topic_name, grade_categories in topics_dict.items():
        topic_filtered = {}
        for category, subtopics in grade_categories.items():
            # Check if this category matches the selected grade
            if selected_grade_display.lower() in category.lower():
                topic_filtered[category] = subtopics
        
        if topic_filtered:  # Only include topics that have content for this grade
            filtered_topics[topic_name] = topic_filtered
    
    return filtered_topics

